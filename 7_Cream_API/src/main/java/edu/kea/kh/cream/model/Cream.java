package edu.kea.kh.cream.model;

public class Cream {

    public int weight;
    public String taste;
    public String color;
    public String name;

    public Cream(String name) {
        this.name = name;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public void setTaste(String taste) {
        this.taste = taste;
    }

    public void setColor(String color) {
        this.color = color;
    }

}
